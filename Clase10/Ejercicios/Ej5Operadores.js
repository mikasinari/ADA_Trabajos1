//Declaración y Asignación de Variables:
//1. Declara y asigna un valor numérico a dos variables: primerNumero y segundoNumero.

let primerNumero = 15; // Primer número
let segundoNumero = 8; // Segundo número

//Operaciones Matemáticas:
//2. Declara las siguientes variables: resultadoSuma, resultadoResta, resultadoMultiplicacion, resultadoDivision.
let resultadoSuma;
let resultadoResta;
let resultadoMultiplicacion;
let resultadoDivision;

//3. Asigna a cada una de estas variables el resultado de la suma, resta, multiplicación y división de las variables primerNumero y segundoNumero.
resultadoSuma = primerNumero + segundoNumero;
resultadoResta = primerNumero - segundoNumero;
resultadoMultiplicacion = primerNumero * segundoNumero;
resultadoDivision = primerNumero / segundoNumero;

//Mostrar Resultados:
//1. Utiliza console.log(nombreDeLaVariable) para imprimir cada uno de los resultados y ver cómo se ven los mismos.

console.log("Resultado de la suma: " + resultadoSuma);
console.log("Resultado de la resta: " + resultadoResta);
console.log("Resultado de la multiplicación: " + resultadoMultiplicacion);
console.log("Resultado de la división: " + resultadoDivision);


